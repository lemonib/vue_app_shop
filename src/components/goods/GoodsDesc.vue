<template>
	<div class="goodsdesc-container">
		<h3>{{info.title}}</h3>
		<hr>
		<div class="content" v-html="info.content"></div>
	</div>
</template>

<script>
	export default{
		data(){
			return{
			id:this.$route.params.id,
			info:{}
			}
		},
		created(){
			this.getGoodsDesc()
		},
		methods:{
			getGoodsDesc(){
				this.$http.get('http://127.0.0.1:4000/goods/getdesc/'+	this.id).then(result=>{
					if(result.body.status===0){
						this.info = result.body.message[0]
					}
				})
			},
		}
	}
</script>

